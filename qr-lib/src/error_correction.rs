use crate::QrVersion;

#[derive(Debug, PartialEq, Eq, Copy, Clone)]
pub enum QrEcLevel {
    L = 0,
    M = 1,
    Q = 2,
    H = 3,
}

#[derive(Debug, PartialEq, Eq, Copy, Clone)]
pub struct QrEc {
    version: QrVersion,
    ec_level: QrEcLevel,
    ec_codewords_per_block: usize,
    groups: &'static [QrEcGroup],
}

impl QrEc {
    fn new(version: QrVersion, ec_level: QrEcLevel, ec_codewords_per_block: usize, groups: &'static [QrEcGroup]) -> Self {
        Self { version, ec_level, ec_codewords_per_block, groups }
    }

    pub fn by(version: QrVersion, ec_level: QrEcLevel) -> QrEc {
        let i = (version.to_u8() * 4 + (ec_level as u8)) as usize;
        ARR[i]
    }
}

